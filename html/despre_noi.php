    <p><b>Cine este My Cinema?</b></p>

    <p>My Cinema este operator de cinematografe multiplex din Romania. Compania detine momentan doua cinematografe mari, in Galati si Bucuresti.
    </p><br/>
    <p><b>Scurt istoric</b></p>
    <p>My Cinema si-a inceput activitatea in Galati, in 2010. In anii care au urmat s-a extins deschizand un nou cinematograf in Bucuresti, in anul 2011.
    </p><br/>
    <p><b>Planuri de dezvoltare</b></p>
    <p>Compania crede ferm in potentialul sau de dezvoltare, cat si in cel al regiunii. Cea mai mare extindere se va produce in Promenada Mall, Braila, la finele anului8 2013.
    </p><br/>
    <p><b>Care sunt caracteristicile multiplexurilor companiei?</b></p>
    <p>Fiecare din multplexurile My Cinema are intre 3 si 23 sali. Toate cinema-urile sunt localizate in orase importante. Politica acestora este sa ruleze cele mai recente filme, cat mai aproape de lansarea lor internationala.
    </p><br/>
    <p><b>My Cinema</b></p>
    <p>16 noiembrie 2010 este o data importanta pentru My CInema reprezentand deschiderea primului sau multiplex
        din Romania, in Dunarea Mall Galati, marcand debutul planurilor sale de dezvoltare in tara. Planurile au continuat in 30 noiembrie cu deschiderea My Cinema Bucuresti, in incinta Afi Palace Cotroceni.
    </p><br/>
