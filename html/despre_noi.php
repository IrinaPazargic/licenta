<h3><strong>Despre Noi</strong></h3><br/>
<div>
    <p><b>Cine este Cinema City?</b></p>

    <p>Cinema City Romania este parte a Cinema City International, cel mai mare operator de cinematografe multiplex
        din Europa Centrala si de Est. Compania detine un lant de cinematografe Cinema City si IMAX速 in Polonia,
        Ungaria, Cehia si Bulgaria. Compania este de asemenea cel mai mare operator de cinema in Israel. Dupa ce s-a
        listat cu success pe bursa din Varsovia, compania a lansat un program de dezvoltare a unei retele extinse si
        in Romania.
    </p><br/>
    <p><b>Scurt istoric</b></p>
    <p>Cinema City International si-a inceput activitatea in Israel, in 1929. In anii care au urmat, a crescut
        exponential, ajungand lider de piata in tara sa de origine. In 1997 a luat decizia de a se extinde si pe
        alte piete, deschizand primul multiplex din afara Israelului, in Ungaria. In cei 14 ani care au trecut de la
        decizia de extindere, compania a ajuns sa detina 95 de cinematografe multiplex, cu un total de 899 sali. La
        acestea se adauga si 9 cinematografe IMAX速 (cinematografie 3D pe ecran cu format mare), pentru care Cinema
        City International detine licenta exclusiva pentru fiecare din tarile in care compania este prezenta. Cinema
        City International este lider atat la nivel regional, cat si in fiecare din piete, si intentioneaza sa-si
        extinda si mai mult prezenta.
    </p><br/>
    <p><b>Planuri de dezvoltare</b></p>
    <p>Compania crede ferm in potentialul sau de dezvoltare, cat si in cel al regiunii. Cea mai mare extindere se va
        produce in Romania, unde compania a deschis 3 multiplexuri in anul 2011, iar planurile in viitorul apropiat
        include alte 8 proiecte in total, in principalele orase ale tarii. Romania va deveni astfel o piata de
        importanta majora pentru Cinema City, cat si pentru industria cinematografica in general.
    </p><br/>
    <p><b>Care sunt caracteristicile multiplexurilor companiei?</b></p>
    <p>Fiecare din multplexurile Cinema City are intre 3 si 23 sali (exceptia fiind un cinematograf cu un singur
        ecran in Tel Aviv). Toate cinema-urile sunt localizate in orase importante. Politica acestora este sa ruleze
        cele mai recente filme, cat mai aproape de lansarea lor internationala. Pe langa cinematografele clasice,
        Cinema City opereaza si IMAX速, cinematografe bazate pe un sistem de proiectie cu lentile duble, capabile de
        crearea unui efect tridimensional, sustinut si de sisteme audio 3D. Noua cinematografe IMAX速 functioneaza
        deja cu success: 5 in Polonia si cate unul 1 in fiecare din tarile urmatoare - Cehia, Bulgaria, Ungaria si
        Romania.
    </p><br/>
    <p><b>Cinema City in Romania</b></p>
    <p>16 noiembrie 2007 este o data importanta pentru Cinema City reprezentand deschiderea primului sau multiplex
        din Romania, in Iulius Mall Iasi, marcand debutul planurilor sale de dezvoltare in tara. Planurile au
        continuat in 30 noiembrie cu deschiderea Cinema City Timisoara, in incinta Iulius Mall Timisoara. Urmatorul
        pas important a fost deschiderea primul cinematograf IMAX din Romania, la Bucuresti in AFI Cotroceni, sub
        denumirea Samsung IMAX si cea mai recenta deschidere a celui de-al 14-lea multiplex local in Ploiesti
        Shopping City.
    </p><br/>
